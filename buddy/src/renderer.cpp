uint32_t rgb565_to_sdl(uint16_t c) {
    uint8_t r = ((c >> 11) & 0x1F) * 255 / 31;
    uint8_t g = ((c >> 5)  & 0x3F) * 255 / 63;
    uint8_t b = (c         & 0x1F) * 255 / 31;
    return (r << 24) | (g << 16) | (b << 8) | 0xFF;
}

Renderer::Renderer(SDL_Renderer* r, int w, int h)
    : ren_(r), w_(w), h_(h), fg_(WHITE), bg_(BLACK), cx_(0), cy_(0), text_size_(1), datum_(TL_DATUM)
{
}

void Renderer::set_draw_color(uint16_t c) {
    uint32_t sdl = rgb565_to_sdl(c);
    SDL_SetRenderDrawColor(ren_, (sdl >> 24) & 0xFF, (sdl >> 16) & 0xFF, (sdl >> 8) & 0xFF, 255);
}

void Renderer::fill_sprite(uint16_t c) {
    set_draw_color(c);
    SDL_RenderClear(ren_);
}

void Renderer::fill_rect(int x, int y, int w, int h, uint16_t c) {
    set_draw_color(c);
    SDL_FRect r = {(float)x, (float)y, (float)w, (float)h};
    SDL_RenderFillRect(ren_, &r);
}

void Renderer::draw_rect(int x, int y, int w, int h, uint16_t c) {
    set_draw_color(c);
    SDL_FRect r = {(float)x, (float)y, (float)w, (float)h};
    SDL_RenderRect(ren_, &r);
}

void Renderer::fill_round_rect(int x, int y, int w, int h, int rad, uint16_t c) {
    fill_rect(x, y, w, h, c);
}

void Renderer::draw_round_rect(int x, int y, int w, int h, int rad, uint16_t c) {
    draw_rect(x, y, w, h, c);
}

void Renderer::draw_fast_hline(int x, int y, int w, uint16_t c) {
    set_draw_color(c);
    SDL_RenderLine(ren_, (float)x, (float)y, (float)(x + w - 1), (float)y);
}

void Renderer::draw_line(int x1, int y1, int x2, int y2, uint16_t c) {
    set_draw_color(c);
    SDL_RenderLine(ren_, (float)x1, (float)y1, (float)x2, (float)y2);
}

void Renderer::fill_circle(int x, int y, int r, uint16_t c) {
    set_draw_color(c);
    for (int dy = -r; dy <= r; dy++) {
        int dx = (int)sqrtf(r*r - dy*dy);
        SDL_RenderLine(ren_, (float)(x - dx), (float)(y + dy), (float)(x + dx), (float)(y + dy));
    }
}

void Renderer::draw_circle(int x, int y, int r, uint16_t c) {
    set_draw_color(c);
    for (int i = 0; i < 360; i++) {
        float a = i * 3.14159f / 180.0f;
        int px = x + (int)(r * cosf(a));
        int py = y + (int)(r * sinf(a));
        SDL_RenderPoint(ren_, (float)px, (float)py);
    }
}

void Renderer::fill_triangle(int x1, int y1, int x2, int y2, int x3, int y3, uint16_t c) {
    set_draw_color(c);
    int min_y = y1 < y2 ? (y1 < y3 ? y1 : y3) : (y2 < y3 ? y2 : y3);
    int max_y = y1 > y2 ? (y1 > y3 ? y1 : y3) : (y2 > y3 ? y2 : y3);
    for (int y = min_y; y <= max_y; y++) {
        int xs[3], n = 0;
        auto edge = [&](int xa, int ya, int xb, int yb) {
            if ((ya <= y && yb > y) || (yb <= y && ya > y)) {
                float t = (float)(y - ya) / (yb - ya);
                xs[n++] = xa + (int)(t * (xb - xa));
            }
        };
        edge(x1, y1, x2, y2);
        edge(x2, y2, x3, y3);
        edge(x3, y3, x1, y1);
        if (n == 2) {
            if (xs[0] > xs[1]) { int t = xs[0]; xs[0] = xs[1]; xs[1] = t; }
            SDL_RenderLine(ren_, (float)xs[0], (float)y, (float)xs[1], (float)y);
        }
    }
}

