void Renderer::string_size(const char* s, int& w, int& h) {
    int len = (int)strlen(s);
    w = len * 6 * text_size_;
    h = 8 * text_size_;
}

void Renderer::print(const char* s) {
    for (const char* p = s; *p; p++) {
        render_char(*p, cx_, cy_);
        cx_ += 6 * text_size_;
    }
}

void Renderer::draw_string(const char* s, int x, int y) {
    int tw, th;
    string_size(s, tw, th);
    int dx = x, dy = y;
    if (datum_ == MC_DATUM) { dx -= tw / 2; dy -= th / 2; }
    set_cursor(dx, dy);
    print(s);
}

void Renderer::printf(const char* fmt, ...) {
    char buf[128];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    print(buf);
}

